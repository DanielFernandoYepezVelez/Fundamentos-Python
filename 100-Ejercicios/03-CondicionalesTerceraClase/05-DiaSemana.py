diaSemana = int(input('Ingrese un Numero equivalente al dia de la semana: '))

if diaSemana == 1:
    print('Hoy es LUNES')
elif diaSemana == 2:
    print('Hoy es MARTES')
elif diaSemana == 3:
    print('Hoy es MIERCOLES')    
elif diaSemana == 4:
    print('Hoy es JUEVES')
elif diaSemana == 5:
    print('Hoy es VIERNES')   
elif diaSemana == 6:
    print('Hoy es SABADO')
elif diaSemana == 7:
    print('Hoy es DOMINGO')    
else:
    print('El numero ingresado es diferente del 1 al 7')